/**
 * 部门
 *
 * @export
 * @interface Dept
 */
export interface Dept {

    /**
     * 负责人
     *
     * @returns {*}
     * @memberof Dept
     */
    manager?: any;

    /**
     * grade
     *
     * @returns {*}
     * @memberof Dept
     */
    grade?: any;

    /**
     * function
     *
     * @returns {*}
     * @memberof Dept
     */
    function?: any;

    /**
     * order
     *
     * @returns {*}
     * @memberof Dept
     */
    order?: any;

    /**
     * path
     *
     * @returns {*}
     * @memberof Dept
     */
    path?: any;

    /**
     * position
     *
     * @returns {*}
     * @memberof Dept
     */
    position?: any;

    /**
     * id
     *
     * @returns {*}
     * @memberof Dept
     */
    id?: any;

    /**
     * 部门名称
     *
     * @returns {*}
     * @memberof Dept
     */
    name?: any;

    /**
     * 上级部门
     *
     * @returns {*}
     * @memberof Dept
     */
    parentname?: any;

    /**
     * parent
     *
     * @returns {*}
     * @memberof Dept
     */
    parent?: any;
}