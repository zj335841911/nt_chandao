/**
 * 附件
 *
 * @export
 * @interface File
 */
export interface File {

    /**
     * 路径
     *
     * @returns {*}
     * @memberof File
     */
    pathname?: any;

    /**
     * 对象ID
     *
     * @returns {*}
     * @memberof File
     */
    objectid?: any;

    /**
     * 已删除
     *
     * @returns {*}
     * @memberof File
     */
    deleted?: any;

    /**
     * 文件类型
     *
     * @returns {*}
     * @memberof File
     */
    extension?: any;

    /**
     * 对象类型
     *
     * @returns {*}
     * @memberof File
     */
    objecttype?: any;

    /**
     * 由谁添加
     *
     * @returns {*}
     * @memberof File
     */
    addedby?: any;

    /**
     * 标题
     *
     * @returns {*}
     * @memberof File
     */
    title?: any;

    /**
     * 添加时间
     *
     * @returns {*}
     * @memberof File
     */
    addeddate?: any;

    /**
     * 下载次数
     *
     * @returns {*}
     * @memberof File
     */
    downloads?: any;

    /**
     * 大小
     *
     * @returns {*}
     * @memberof File
     */
    size?: any;

    /**
     * id
     *
     * @returns {*}
     * @memberof File
     */
    id?: any;

    /**
     * 备注
     *
     * @returns {*}
     * @memberof File
     */
    extra?: any;
}